public class Reverse {
    public static void main(String[] args) {
        int n = 0;
        int i;
        int j;

        for(i = 10; i >= n; i-- ){ //print the numbers in reverse
            System.out.print(i + " ");
        }
        System.out.println(" ");
        int k = 10;
        for(j = 0; j <= k; j++){ //print numbers in order
            System.out.print(j + " ");
        }
    }
}
